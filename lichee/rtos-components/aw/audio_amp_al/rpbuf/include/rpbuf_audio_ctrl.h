#ifndef __RPBUF_AUDIO_CRTL_H__
#define __RPBUF_AUDIO_CRTL_H__

#define RPBUF_LEN_MAX			(0x100000)

#define RPBUF_CTRL_ID					(0)

typedef enum {
	RPBUF_CTL_NONE 	= 0,
	RPBUF_CTL_INIT,
	RPBUF_CTL_CONFIG,            // config algo param
	RPBUF_CTL_START,             // start algo
	RPBUF_CTL_PAUSE,             // pause algo
	RPBUF_CTL_RESUME,            // resume algo
	RPBUF_CTL_STOP,              // stop algo
	RPBUF_CTL_RELEASE,           // release algo
	RPBUF_CTL_ENABLE_ALG,        // enable algorithm
	RPBUF_CTL_DUMP_MERGE_DATA,   // input data(2 mic + 1 ref/ 6ch eq) + output data (aec / 6ch eq)
	RPBUF_CTL_START_ACK,         // start algo ack
	RPBUF_CTL_NUM,
}RPBUF_CTRL_TYPE;

typedef enum {
	RPBUF_ALGO_NONE 	= 0,
	RPBUF_ALGO_AEC,            // aec algo
	RPBUF_ALGO_AEC_DOWNLINK,   // aec downlink algo param
	RPBUF_ALGO_EQ,             // eq algo param
	RPBUF_ALGO_AMP,            // audio module process algo param
	RPBUF_ALGO_NUM,
}RPBUF_ALGO_TYPE;

/** The RPBUF_ERROR_TYPE enumeration defines the standard RPBUF Errors.
 *  These errors should cover most of the common failure cases.
 */
typedef enum RPBUF_ERROR_TYPE
{
	RPBUF_ERROR_NONE = 0,

	RPBUF_ERROR_INIT_FAIL = (long) 0x60001000,

	RPBUF_ERROR_CONFIG_FAIL = (long) 0x60001001,

	RPBUF_ERROR_START_FAIL = (long) 0x60001002,

	RPBUF_ERROR_PAUSE_FAIL = (long) 0x60001003,

	RPBUF_ERROR_RESUME_FAIL = (long) 0x60001004,

	RPBUF_ERROR_STOP_FAIL = (long) 0x60001005,

	RPBUF_ERROR_RELEASE_FAIL = (long) 0x60001006,

	RPBUF_ERROR_ENABLE_ALG_FAIL = (long) 0x60001007,

	RPBUF_ERROR_DUMP_MERGE_DATA = (long) 0x60001008,

	/** There was an error, but the cause of the error could not be determined */
	RPBUF_ERROR_UNDEFINED = (long)0x70000000,
	RPBUF_ERROR_MAX = 0x7FFFFFFF
} RPBUF_ERROR_TYPE;



#endif
