#define DRQDST_TWI1_TX		5
#define DRQDST_TWI2_TX		6
#define DRQDST_R_SPI		7
#define DRQDST_R_I2S0_TX	8
#define DRQDST_UART2_TX		10

#endif /*__DMA_SUN20IW2_H__  */
