#define DMA_IRQ_NUM		RINTC_IRQ_MASK | 21	/* MCU DMA 0~7 channel irq non-secure */
#define DMA_SECURE_IRQ_NUM	RINTC_IRQ_MASK | 22	/* MCU DMA 0~7 channel irq secure */
