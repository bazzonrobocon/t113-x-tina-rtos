#define SUNXI_DMAC_PBASE	(0x07150000ul)
#endif

#define DMA_IRQ_V2
#define DMA_IRQ_EN(x)		(SUNXI_DMAC_PBASE + (0x134 + ((x + START_CHAN_OFFSET) << 6)))
#define DMA_IRQ_STAT(x)		(SUNXI_DMAC_PBASE + (0x138 + ((x + START_CHAN_OFFSET) << 6)))
/*
 * The source DRQ type and port corresponding relation
 */
